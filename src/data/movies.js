export const getMovieById = (id) => {
    return movies.find(movie => movie.id === parseInt(id))
}

export const filterMovies = (filters) => {
    return movies.filter(movie => {
        const genreMatch = !filters.genre || movie.genre.includes(filters.genre)
        const languageMatch = !filters.language || movie.language === filters.language
        const ratingMatch = !filters.rating || movie.rating >= parseFloat(filters.rating)
        return genreMatch && languageMatch && ratingMatch
    })
}
